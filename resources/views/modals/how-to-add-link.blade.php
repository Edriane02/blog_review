                <h4>Steps to Add and Format a Link</h4>
                <ol>
                    <li><strong>Highlight the text</strong> you want to turn into a link.</li>
                    <ul>
                        <li>Example: Highlight <strong>"Author's page on Amazon:"</strong> and make it <strong>bold</strong> for better readability.</li>
                    </ul>

                    <li><strong>Insert the link:</strong></li>
                    <ul>
                        <li>Highlight the URL (e.g., <code>https://www.amazon.com/...</code>).</li>
                        <li>Right-click on it and select <strong>"Link"</strong> from the menu.</li>
                        <li>A pop-up will appear.</li>
                    </ul>

                    <li><strong>Configure the link settings:</strong></li>
                    <ul>
                        <li><strong>URL field:</strong> Paste the link.</li>
                        <li><strong>Text to display field:</strong></li>
                        <ul>
                            <li>You can leave the full link as is or replace it with a custom word (e.g., "Amazon Page").</li>
                        </ul>
                        <li><strong>Title field:</strong> Leave it blank (optional).</li>
                        <li><strong>Open link in... field:</strong> Select <strong>"New Window"</strong> for better user experience.</li>
                        <li>Click <strong>Save</strong>.</li>
                    </ul>

                    <li><strong>Repeat these steps</strong> for other links as needed, such as their <strong>books</strong>, <strong>socials</strong>, or whatever else you want to link.</li>
                </ol>

